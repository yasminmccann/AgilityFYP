package com.example.agilityfyp;

public class Circle2 {

    int X;
    int Y;
    int Width;
    int Height;

    public Circle2(int X, int Y, int Width, int Height) {
        this.X = X;
        this.Y = Y;
        this.Height = Height;
        this.Width = Width;
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }

    public int getWidth() {
        return Width;
    }

    public void setWidth(int width) {
        Width = width;
    }

    public int getHeight() {
        return Height;
    }

    public void setHeight(int height) {
        Height = height;
    }
}
